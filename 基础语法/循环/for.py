# range 函数

listA = range(1,5)

# 1, 2, 3, 4

for number in listA:
    print(number)

print("----------------------------------")

listB = range(3)

# 0, 1, 2

for number in listB:
    print(number)

print("----------------------------------")

listC = range(0, 10, 2)

# 步长
# 0, 2, 4, 6, 8

for number in listC:
    print(number)


